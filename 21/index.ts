// Define objects containing various items
const person = {
    firstName: 'John',
    lastName: 'Doe',
    age: 30,
    occupation: 'Software Developer'
};

const car = {
    make: 'Honda',
    model: 'Civic',
    year: 2022,
    color: 'Silver'
};

const book = {
    title: 'The Great Gatsby',
    author: 'F. Scott Fitzgerald',
    publishedYear: 1925
};

// Access and display information from the objects
console.log(`Person: ${person.firstName} ${person.lastName}, Age: ${person.age}, Occupation: ${person.occupation}`);
console.log(`Car: ${car.year} ${car.make} ${car.model}, Color: ${car.color}`);
console.log(`Book: ${book.title} by ${book.author}, Published in ${book.publishedYear}`);
